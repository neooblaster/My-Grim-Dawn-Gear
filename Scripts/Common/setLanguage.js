function setLanguage(language){
	/** Traiter l'URL pour l'envoyer en tant que code retour **/
	var url = document.location.href.split('#');
	
	/** Requete de modification de la langue **/
	var xQuery = new xhrQuery();
		xQuery.target('/XHR/Common/set_language.php');
		xQuery.values('LANG_KEY='+language);
		xQuery.callbacks(
			// Rechargement de la page
			function(e){
				document.location.href = e;
			}
		);
		xQuery.send();
}







function duplicateItem(id, extend){
	if(confirm("Do you want to copy specified extension '"+extend+"' ?")){
		var xQuery = new xhrQuery();
		xQuery.target("/XHR/Admin/duplicate_item.php");
		xQuery.values("id="+id, "extend="+extend);
		xQuery.callbacks(function(e){
			e = JSON.parse(e);
			
			window.open("/manage/edit/items/"+e.ID);
		});
		xQuery.send();
	}
}
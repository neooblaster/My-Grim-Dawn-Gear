function load_items_attachments(){
	global $PDO;
	global $vars;
	
	try {
		$qItemsAttachments = $PDO->query("SELECT ID, ATTACHMENT FROM ATTACHMENTS");
		
		$ITEMS_ATTACHMENTS = Array();
		
		while($faItemsAttachements = $qItemsAttachments->fetch(PDO::FETCH_ASSOC)){
			$ITEMS_ATTACHMENTS[] = Array(
				"ID" => $faItemsAttachements['ID'],
				"ATTACHMENT" => $faItemsAttachements['ATTACHMENT']
			);
		}
		
		return $ITEMS_ATTACHMENTS;
	} catch(Exception $e){
		return Array();
	}
}

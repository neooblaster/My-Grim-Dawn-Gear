/** Déclaration de la fonction de lecture et de compilation **/
ob_start();

function compile_less($folder_path){
	global $LESS_files;
	
	$directory = scandir($folder_path);
	$exclude_values = Array('.', '..');
	
	foreach($directory as $key => $value){
		$full_path = $folder_path.'/'.$value;
		
		if(!in_array($value, $exclude_values)){
			if(is_dir($full_path)){
				compile_less($full_path);
			} else {
				$file = fopen($full_path, 'r');
				$compile_instruction = fgets($file);
				fclose($file);
				
				if(preg_match('#^\/\/\#\!\/#', $compile_instruction)){
					$compile_instruction = str_replace('//#!/', '', $compile_instruction);
					
					$matches = Array();
					
					preg_match('#^(compile)\s?=\s?(true|false)#i', $compile_instruction, $matches);
					
					if(strtolower($matches[1]) === 'compile' && strtolower($matches[2]) === 'true'){
						$output_path = preg_replace('#\/LESS\/#i', '/CSS/', $folder_path);
						$output_file = preg_replace('#\.less$#i', '.css', $value);
						
						$LESS_files[] = $full_path;
						
						echo "Executing : \n";
						echo "------------\n";
						echo $cmd = "sudo lessc --clean-css $full_path $output_path/$output_file 2>&1";
						//$cmd = "ll 2>&1";
						echo "\n\n";
						echo shell_exec($cmd);
						echo "\n\n";
					}
				}
			}
		}
	}
}

